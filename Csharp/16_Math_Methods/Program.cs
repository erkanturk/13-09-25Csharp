namespace _16_Math_Methods
{
    internal class Program
    {
        static void Main(string[] args)
        {
            //Matematiksel Hazır Methodlar
            Console.WriteLine($"Pi Sayısı: {Math.PI}");
            Console.WriteLine($"Karekök Alma: {Math.Sqrt(25)}");
            Console.WriteLine($"Kuvvetini Alma: {Math.Pow(2, 5)}");
            Console.WriteLine($"Mutlak değer Alma: {Math.Abs(-30)}");
            Console.WriteLine($"Max değer Alma: {Math.Max(20,60)}");
            Console.WriteLine($"Min değer Alma: {Math.Min(20, 60)}");
            Console.WriteLine($"Tam Yuvarlama: {Math.Round(20.49)}");
            Console.WriteLine($"Yukarı Yuvarlama: {Math.Ceiling(20.01)}");
            Console.WriteLine($"Aşağı Yuvarlama: {Math.Floor(20.99)}");



        }
    }
}
